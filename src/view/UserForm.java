    private void loadRolesToComboBox() {
    try {
        Connection conn = DBConnection.getConnection();
        String sql = "SHOW COLUMNS FROM pengguna LIKE 'role'";
        Statement stmt = conn.createStatement();
        ResultSet rs = stmt.executeQuery(sql);

        if (rs.next()) {
            String type = rs.getString("Type"); // misalnya: enum('admin','guru')
            type = type.replace("enum(", "").replace(")", "").replace("'", "");
            String[] roles = type.split(",");

            jComboBox1.removeAllItems();
            for (String role : roles) {
                jComboBox1.addItem(role.trim());
            }
            jComboBox1.setSelectedIndex(-1);
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Gagal load roles: " + e.getMessage());
    }
    }
    
    private void tampilkanDataPengguna() {
        DefaultTableModel model = new DefaultTableModel(new String[]{"ID", "Username", "Role"}, 0);
        jTable1.setModel(model);

        try {
            Connection conn = DBConnection.getConnection();
            String sql = "SELECT id_user, username, role FROM pengguna";
            PreparedStatement stmt = conn.prepareStatement(sql);
            ResultSet rs = stmt.executeQuery();

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("id_user"),
                    rs.getString("username"),
                    rs.getString("role")
                });
            }

            // Optional: sembunyikan kolom ID
            jTable1.getColumnModel().getColumn(0).setMinWidth(0);
            jTable1.getColumnModel().getColumn(0).setMaxWidth(0);
            jTable1.getColumnModel().getColumn(0).setWidth(0);

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal menampilkan data: " + e.getMessage());
        }
    }


