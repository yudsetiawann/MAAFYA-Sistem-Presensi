    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String username = jTextField1.getText();
        String passwordPlain = new String(jPasswordField1.getPassword());
        String password = HashUtil.hashPassword(passwordPlain);
        String role = (String) jComboBox1.getSelectedItem();

        if (username.isEmpty() || password.isEmpty() || role == null) {
            JOptionPane.showMessageDialog(this, "Semua field harus diisi.");
            return;
        }

        try {
            Connection conn = DBConnection.getConnection();
            String sql = "INSERT INTO pengguna (username, password, role) VALUES (?, ?, ?)";
            PreparedStatement stmt = conn.prepareStatement(sql);
            stmt.setString(1, username);
            stmt.setString(2, password); // untuk produksi, sebaiknya di-hash
            stmt.setString(3, role);
            stmt.executeUpdate();

            JOptionPane.showMessageDialog(this, "Pengguna berhasil disimpan.");
            jTextField1.setText("");
            jPasswordField1.setText("");
            jComboBox1.setSelectedIndex(-1);
            tampilkanDataPengguna();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal menyimpan: " + e.getMessage());
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

